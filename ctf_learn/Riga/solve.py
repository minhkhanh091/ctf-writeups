key = [0x9f,0xae,0x9c,0xb6,0xbd,0xb9,0xef,0xeb,0xe6,0x9e,0xb9,0xec,0xb3,
       0xb9,0xe3,0xb9,0xbb,0xa8,0x89,0xe3,0xbd,0xef,0xbb,0x96,0xb9,0xed,
       0xe3,0x89,0xb9,0xe4]

result = ""

for target in key:
    for c in range(0x20, 0x7f):
        b1 = (c + 0x11) & 0xff
        b3 = (c + 0xb2) & 0xff

        if b1 < 0x7f:
            if (b1 ^ 0xcb) == target:
                result += chr(c)
                break
        else:
            if ((b3 ^ 0xcb) & 0xff) == target:
                result += chr(c)
                break

print(result)
